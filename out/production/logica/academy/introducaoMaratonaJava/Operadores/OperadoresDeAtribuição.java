package MaratonaJava.introducaoMaratonaJava.Operadores;

public class OperadoresDeAtribuição {
    public static void main(String[] args) {
        // =, -=, +=, *=, /=, %=
        int salario = 1800;
        // = salario + 1000;
        salario += 1000;
        System.out.println( salario );
    }
}
